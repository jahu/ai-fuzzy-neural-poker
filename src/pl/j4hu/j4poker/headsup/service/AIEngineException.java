package pl.j4hu.j4poker.headsup.service;

public class AIEngineException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AIEngineException() {
		super();
	}

	public AIEngineException(String message, Throwable cause) {
		super(message, cause);
	}

	public AIEngineException(String message) {
		super(message);
	}

	public AIEngineException(Throwable cause) {
		super(cause);
	}
}
